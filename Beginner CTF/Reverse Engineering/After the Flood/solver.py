def ror8(x, r):
    r &= 7
    return ((x >> r) | (x << (8 - r))) & 0xFF

K = [0xA9, 0x13, 0x7C, 0x21, 0x5E, 0xC3]

C_EVEN = [0xEA,0x45,0x09,0x95,0xF9,0x3E,0x1D,0x38,0xFA,0x66,0xAA,0x2F,0x6D,0x0C,0x9B,0xDD,0x9F,0x04,0xDE,0x96,0xCE,0xE7,0x8B,0xE4,0x81,0x54,0x48,0x9A,0x70,0x4F,0x4B]
C_ODD  = [0x9E,0x15,0x33,0xC3,0x36,0xFB,0x19,0x3E,0x6D,0xC1,0xA9,0x23,0x07,0xE7,0x01,0xCE,0x24,0x71,0xF1,0x06,0xF4,0x01,0x31,0x84,0xFF,0x25,0xEC,0xB4,0x94,0x5B,0xEF]

enc = [None] * 62
e = o = 0
for i in range(62):
    enc[i] = C_ODD[o] if (i & 1) else C_EVEN[e]
    if i & 1: o += 1
    else:     e += 1

flag = ""
for i in range(62):
    ks = ror8(K[i % 6] ^ (i & 0xFF), i & 7)
    x  = enc[i] ^ ((i * 29) & 0xFF)
    x  = ror8(x, (i * 3) & 7)
    flag += chr(x ^ ks)

print(flag)